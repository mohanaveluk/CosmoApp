create  procedure [dbo].[CWT_SetPortalNextJobRunTime]
(
	@ID int
	,@LASTJOBRANTIME datetime
	,@NEXTJOBRUNTIME datetime
)
As 
Begin
	update [dbo].[CSM_URLCONFIGURATION] set
	[URL_LASTJOBRUNTIME] = @LASTJOBRANTIME
	,[URL_NEXTJOBRUNTIME] = @NEXTJOBRUNTIME
	where [URL_ID] = @ID
End

GO


